
create table admin(
    admin_idx int primary key auto_increment,
    admin_id varchar(255),
    admin_pwd varchar(255)
);

create table student(
    student_idx int primary key auto_increment,
    major_idx int,
    lecturer_idx,
    student_id varchar(255),
    student_pwd varchar(255),
    student_name varchar(255),
    student_year int,
    student_sex varchar(255),
    student_state varchar(255),
);

create table registration(
    student_idx int,
    class_idx int
);

create table grade(
    grade_idx int primary key auto_increment,
    student_idx int,
    course_idx int,
    grade_year int,
    grade_grade
);

create table course(
    course_idx int primary key auto_increment,
    course_id varchar(255),
    course_name varchar(255),
    course_credit int,
    course_year int
);

create table class(
    class_idx int primary key auto_increment,
    course_idx int,
    lecturer_idx int,
    room_idx int,
    class_no int,
    class_register int,
    class_max int,
    class_opened int,
    class_begin varchar(255),
    class_end varchar(255),
)

create table lecturer(
    lecturer_idx int primary key auto_increment,
    major_idx int,
    lecturer_id varchar(255),
    lecturer_name varchar(255)
);

create table major(
    major_idx int primary key,
    major_name varchar(255)
);

create table room(
    room_idx int primary key,
    room_building_name varchar(255),
    room_name varchar(255),
    room_occupancy int
);
